# String manipulation
#
movie = 'The Lord of the Rings: The Return of the King'

print('Number of characters:', len(movie))
print('Title in capital letters', movie.upper())
print('Title in small letters', movie.lower())
print('Number of "e" in the title', movie.count('e'))
print('First occurrence of the word "Lord"', movie.find('Lord'))  
print('First occurrence of the word "dragon"', movie.find('dragon'))  