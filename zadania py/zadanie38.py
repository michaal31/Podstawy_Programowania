###
# Calculation of circle area and circumferences
#

# determine radius and PI values
# calculate area 
# calculate circumference 
# print results
radius = int(input('Enter radius of circle: '))
pi = 3.14
area = pi * radius ** 2
circumference = 2 * pi * radius
print(f'Area = {area}')
print(f'Circumference = {circumference}')
